/**
 * 生成卷纲标准化 Markdown 模板。
 *
 * @param volumeNumber - 卷号
 * @param title - 卷标题
 * @param chapterRange - 章节范围描述（如 "第1-20章"）
 * @returns 卷纲 Markdown 字符串
 */
export function getVolumeOutlineTemplate(
  volumeNumber: number,
  title: string,
  chapterRange: string,
): string {
  return [
    `# 卷纲_第${volumeNumber}卷${title ? `-${title}` : ""}.md`,
    "",
    "---",
    "",
    "## 核心信息表",
    "",
    "| 项目 | 内容 |",
    "| --- | --- |",
    `| 章节范围 | ${chapterRange} |`,
    "| 本卷定位 | [填写本卷在整体故事中的定位] |",
    "| 情绪基调 | [填写本卷整体的情绪基调] |",
    "",
    "## 本卷目标",
    "",
    "[主角在本卷结束时达到什么状态]",
    "",
    "## 本卷核心矛盾",
    "",
    "[本卷最核心的冲突/矛盾]",
    "",
    "## 对标结构坐标",
    "",
    "| 节点 | 章节 | 内容 |",
    "| --- | --- | --- |",
    "| 1/4 节点 | [章节号] | [第一个重要转折] |",
    "| 中点 | [章节号] | [本卷中点关键事件] |",
    "| 3/4 节点 | [章节号] | [推进至最终高潮的关键转折] |",
    "| 高潮节点 | [章节号] | [本卷最高潮] |",
    "",
    "## 情绪弧线",
    "",
    "[W形折线描述：起点→下降→回升→再降→最终回升]",
    "",
    "## 爽点节奏表",
    "",
    "| 章节区间 | 爽点类型 | 具体内容 | 预期效果 |",
    "| --- | --- | --- | --- |",
    `| ${chapterRange} | | | |`,
    "",
    "## 卷节拍表",
    "",
    "| 节拍位置 | 章节范围 | 关键事件 | 情绪目标 | 爽点/压迫点 | 承接作用 |",
    "| --- | --- | --- | --- | --- | --- |",
    `| 开卷 | ${chapterRange} | | | | |`,
    "| 中段推进 | [章节范围] | | | | |",
    "| 高压转折 | [章节范围] | | | | |",
    "| 卷末高潮 | [章节范围] | | | | |",
    "",
    "## 卷时间线",
    "",
    "| 时间锚点 | 章节范围 | 发生事件 | 角色状态变化 | 伏笔/设定变化 | 与前后卷承接 |",
    "| --- | --- | --- | --- | --- | --- |",
    "| [起始时间] | [章节范围] | | | | |",
    "| [关键时间点] | [章节范围] | | | | |",
    "| [结束时间] | [章节范围] | | | | |",
    "",
    "## 章节定位分布表",
    "",
    "| 章 | 定位 | 压力级(1-5) | 情绪强度(1-10) | 说明 |",
    "| --- | --- | --- | --- | --- |",
    "",
    "## 人物弧线",
    "",
    "[主要角色在本卷的成长/变化]",
    "",
    "## 本卷伏笔",
    "",
    "| ID | 内容 | 优先级 | 预计回收时机 |",
    "| --- | --- | --- | --- |",
    "",
    "## 本卷关键反转",
    "",
    "[本卷至少一个关键反转]",
    "",
    "## 结构节点建议",
    "",
    "[基于当前结构给出的节点写作建议]",
    "",
  ].join("\n");
}

