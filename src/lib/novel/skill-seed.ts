1. **话不说满**：不要让人物把心里想的全说出来
2. **答非所问**：不正面回答问题，转移话题，说明有隐情
3. **重复强调**：反复说某件事，可能是在掩饰相反的事
4. **语气变化**：同样的话，不同的语气有不同的意思
5. **动作配合**：说话时的小动作、表情，补充潜台词

